    public ArrayList<ObjetoDelJuego> getLadrillos(){
        for(fila = 0; fila < numeroFilas; fila++){
            for(columna = 0; columna < numeroColumnas; columna++){
                if(mapa[fila][columna] == 1){
                    ladrillo = new LadrilloDestructible(new Vector2D(columna*32, fila*32), Assets.ladrillo[0], estadoDeJuego);
                    ladrillos.add(ladrillo);
                    
                }
                if(mapa[fila][columna] == 2){
                    enemigo = new Enemigo(new Vector2D(columna*32, fila*32), Assets.enemigo[0], estadoDeJuego, Assets.enemigo, this);
                    ladrillos.add(enemigo);
                }
                if(mapa[fila][columna] == 3){
                    moneda = new Monedas(new Vector2D(columna*32, fila*32), Assets.moneda[1], estadoDeJuego, Assets.moneda);
                    ladrillos.add(moneda);
                }
                if(mapa[fila][columna] == 4){
                    ladrilloIndestructible = new LadrilloIndestructible(new Vector2D(columna*32, fila*32), Assets.ladrilloIndestructible, estadoDeJuego);
                    ladrillos.add(ladrilloIndestructible);
                }
                if(mapa[fila][columna] == 9){
                    laCaja = new LaCaja(new Vector2D(columna*32, fila*32), Assets.cajaPregunta[0], estadoDeJuego, Assets.cajaPregunta, Assets.cajaVacia);
                    ladrillos.add(laCaja);
                }
                if(mapa[fila][columna] == 10){
                    ladrilloIndestructible = new LadrilloIndestructible(new Vector2D(columna*32, fila*32), Assets.canoChico, estadoDeJuego);
                    ladrillos.add(ladrilloIndestructible);
                }
                if(mapa[fila][columna] == 11){
                    ladrilloIndestructible = new LadrilloIndestructible(new Vector2D(columna*32, fila*32), Assets.canoMediano, estadoDeJuego);
                    ladrillos.add(ladrilloIndestructible);
                }
                if(mapa[fila][columna] == 12){
                    ladrilloIndestructible = new LadrilloIndestructible(new Vector2D(columna*32, fila*32), Assets.canoGrande, estadoDeJuego);
                    ladrillos.add(ladrilloIndestructible);
                }
                if(mapa[fila][columna] == 6){
                    adorno = new Adornos(new Vector2D(columna*32, fila*32), Assets.nube, estadoDeJuego);
                    ladrillos.add(adorno);
                }
                if(mapa[fila][columna] == 7){
                    adorno = new Adornos(new Vector2D(columna*32, fila*32), Assets.castillo, estadoDeJuego);
                    ladrillos.add(adorno);
                }
                if(mapa[fila][columna] == 8){
                    adorno = new Adornos(new Vector2D(columna*32, fila*32), Assets.colina, estadoDeJuego);
                    ladrillos.add(adorno);
                }
                
                
            }
        }
        return ladrillos;
    }
